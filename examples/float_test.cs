// Test floating-point numbers

print("=== Float Literals ===");
print(3.14);
print(2.5);
print(0.5);
print(100.0);

print("\n=== Float Arithmetic ===");
print("3.14 + 2.86 =", 3.14 + 2.86);
print("10.5 - 3.2 =", 10.5 - 3.2);
print("2.5 * 4.0 =", 2.5 * 4.0);
print("10.0 / 4.0 =", 10.0 / 4.0);

print("\n=== Mixed Int/Float ===");
print("5 + 2.5 =", 5 + 2.5);
print("10 - 3.7 =", 10 - 3.7);
print("3 * 2.5 =", 3 * 2.5);
print("10 / 4.0 =", 10 / 4.0);

print("\n=== Comparisons ===");
print("3.14 > 2.5:", 3.14 > 2.5);
print("1.5 < 2.5:", 1.5 < 2.5);
print("3.0 == 3:", 3.0 == 3);
print("2.5 >= 2.5:", 2.5 >= 2.5);

print("\n=== Negative ===");
print(-3.14);
print(-(2.5 + 1.5));

print("\n=== Scientific Notation ===");
print(1e3);
print(1.5e2);
print(2e-3);

print("\n=== String Concatenation ===");
print("Pi is approximately " + 3.14159);
print("Value: " + (10.5 + 2.3));

print("\n=== typeof ===");
print("typeof(3.14):", typeof(3.14));
print("typeof(42):", typeof(42));
print("typeof(\"hi\"):", typeof("hi"));
